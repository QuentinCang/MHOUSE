<div class="block" id="block_faq">
    <h2> Questions régulièrement posées :</h2>
    <p><strong>Comment faire pour s'inscrire/se connecter sur M'House ?</strong></p>
    <p><em>Afin de vous connecter sur le site, vous pouvez cliquer sur "Inscription" si vous n'êtes pas encore inscrit ou vous pouvez cliquer sur "Connexion" lorsque vous êtes déjà inscrit.</em></p>
    <p><strong>Modifier son mot de passe</strong></p>
    <p><em>Pour modifier votre mot de passe, il faut tout d'abord que vous soyez connecté. Ensuite vous devez cliquer sur l'onglet "Profil" et vous pourrez ainsi changer votre mot de passe dans le formulaire "Modifier profil existant". </em></p>
    <p><strong>Ajouter une nouvelle maison</strong></p>
    <p><em>Pour ajouter une nouvelle maison, que ce soit une deuxième ou votre première, il vous faut cliquer sur l'onglet "Maison" puis sur "Modifier mes maisons". Vous aurez ensuite de nouveau un formulaire afin d'ajouter une maison.</em></p>
    <p><strong>Ajouter un nouveau capteur</strong></p>
    <p><em>Pour ajouter un nouveau capteur, vous devez cliquer sur l'onglet "Capteur", choisir le domicile dans lequel vous voulez ajouter ce nouveau capteur et ensuite remplir le formulaire et toutes les informations nécessaire.</em></p>
    <p><strong>Ajouter un compte enfant</strong></p>
    <p><em>Pour ajouter un compte enfant, il vous suffit d'aller dans l'onglet "Profil" et vous trouverez en bas de la page le formulaire pour ajouter un compte enfant.</em></p>
    <p><strong>Voir les capteurs de sa maison</strong></p>
    <p><em>Pour visualiser les différents capteurs présent dans votre maison, cliquez sur l'onglet 'Maison", choisissez votre maison et tous les capteurs ainsi que leurs informations seront affichés.</em></p>

</div>
